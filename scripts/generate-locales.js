for (const [langCode, messages] of Object.entries(translations)) {
    const langDir = path.join(localesDir, langCode);
    if (!fs.existsSync(langDir)) {
        fs.mkdirSync(langDir);
    }

    const messagesJson = {};
    
    // 英語のキーをベースに生成
    for (const [key, value] of Object.entries(enMessages)) {
        if (messages[key]) {
            messagesJson[key] = {
                "message": messages[key],
                "description": value.description
            };
            if (value.placeholders) {
                messagesJson[key].placeholders = value.placeholders;
            }
        } else {
            // 翻訳がない場合は英語をフォールバックとして使用
            console.warn(`Missing translation for ${langCode}: ${key}`);
            messagesJson[key] = value;
        }
    }

    fs.writeFileSync(
        path.join(langDir, 'messages.json'),
        JSON.stringify(messagesJson, null, 2)
    );
    
    console.log(`Generated ${langCode}/messages.json`);
}

